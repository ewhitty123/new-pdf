def user_prompt():
    print("=" * 40)
    print("Press 1 to Combine PDFs.")
    print("Press 2 to Create PDF Bookmarks.")
    print("Press 3 to do both!")
    print("=" * 40)
    print("\n")

    return input("User Choice: ").strip()


def validate_prompt():
    while True:
        mode_choice = user_prompt()

        if mode_choice == "1":
            return 1

        elif mode_choice == "2":
            return 2

        elif mode_choice == "3":
            return 3

        else:
            print("\n")
            print(f"Please try again. '{mode_choice}' is not an option.")
            print("\n")


def yes_no_prompt(prompt: str) -> bool:
    while True:
        choice = input(prompt).strip().lower()

        if choice in {"y", "yes"}:
            return True

        if choice in {"n", "no"}:
            return False

        print("Please enter yes or no.\n")


def clean_path_input(user_input: str) -> str:
    """
    Cleans a user-entered path.

    Allows paths wrapped in matching single or double quotes:
        "/path/to/file.pdf"
        '/path/to/file.pdf'

    It only removes quotes if they wrap the entire path.
    """
    path = user_input.strip()

    if len(path) >= 2 and path[0] == path[-1] and path[0] in {"'", '"'}:
        path = path[1:-1].strip()

    return path


def is_pdf(filepath: str) -> bool:
    path = Path(filepath)

    if not path.is_file():
        return False

    if path.suffix.lower() != ".pdf":
        return False

    try:
        with path.open("rb") as f:
            header = f.read(4)
            return header == b"%PDF"
    except OSError:
        return False


def is_csv(filepath: str) -> bool:
    path = Path(filepath)

    if not path.is_file():
        return False

    if path.suffix.lower() != ".csv":
        return False

    try:
        with path.open(newline="", encoding="utf-8") as csvfile:
            reader = csv.DictReader(csvfile)

            if reader.fieldnames is None:
                return False

            required_columns = {"title", "page"}
            return required_columns.issubset(set(reader.fieldnames))

    except Exception:
        return False


def get_pdf_path(prompt="Enter a PDF filepath: ") -> str:
    while True:
        user_path = clean_path_input(input(prompt))

        if is_pdf(user_path):
            return user_path

        print("Invalid PDF. Please try again.\n")


def get_csv_path() -> str:
    while True:
        user_path = clean_path_input(input("Enter a CSV filepath: "))

        if is_csv(user_path):
            return user_path

        print("Invalid CSV. Please try again.\n")


def get_output_pdf_path(prompt="Enter the path for the output PDF, including .pdf extension: ") -> str:
    while True:
        output_path = clean_path_input(input(prompt))
        path = Path(output_path)

        if path.suffix.lower() != ".pdf":
            print("Output file must end in .pdf. Please try again.\n")
            continue

        if path.exists() and path.is_dir():
            print("Output path is a directory. Please provide a full filename.\n")
            continue

        if path.parent != Path(".") and not path.parent.exists():
            print("The output folder does not exist. Please try again.\n")
            continue

        return output_path


